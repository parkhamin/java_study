package Chapter01;

public class Variables_2 {
    public static void main(String[] args){
        boolean myBoolean = true;
        myBoolean = false;

        int myInt = 123; // 정수형 자료형

        long myLong = 12345678910L; // 큰 값인 걸 표시하기 위해 자료형 long 사용. 단, 숫자 뒤에 L을 붙임.

        double myDouble = 3.14; // 소수형 자료형(float 보다 더 정밀한 소수형 double)

        String myString = "Hello!";

        System.out.println("Hello!");

        String hello = "Hello, I'm ";
        String dot = ".";

        System.out.print(hello);
        System.out.print(26);
        System.out.println(dot);
    }
}

